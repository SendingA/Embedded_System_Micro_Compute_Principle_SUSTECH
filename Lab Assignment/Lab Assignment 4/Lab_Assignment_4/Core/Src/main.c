// 检测滑动方向
SlideDirection Detect_Slide_Direction(int16_t start_x, int16_t start_y, int16_t current_x, int16_t current_y) {
    int16_t dx = current_x - start_x;
    int16_t dy = current_y - start_y;

    // 设置滑动阈值，避免误判
    int16_t threshold = 20; // 像素

    if (abs(dx) > abs(dy)) {
        if (dx > threshold) {
            return SLIDE_RIGHT; // 向右滑动
        } else if (dx < -threshold) {
            return SLIDE_LEFT; // 向左滑动
        }
    } else {
        if (dy > threshold) {
            return SLIDE_DOWN; // 向下滑动
        } else if (dy < -threshold) {
            return SLIDE_UP; // 向上滑动
        }
    }

    return SLIDE_NONE; // 未检测到滑动
}

