// 添加滑动事件到队列
void Record_Slide_Event(SlideDirection direction) {
    uint32_t timestamp = HAL_GetTick();
    slide_history[slide_index].timestamp = timestamp;
    slide_history[slide_index].direction = direction;
    slide_index = (slide_index + 1) % SLIDE_HISTORY_SIZE;
    // 更新最后滑动时间
    last_slide_time = timestamp;
}


// 分析滑动事件，计算速度增量
int Analyze_Slide_Events(void) {
    uint32_t now = HAL_GetTick();
    int16_t delta_speed = 0;

    // 遍历滑动事件队列，计算在 timeout 窗口内的速度增量
    for (uint8_t i = 0; i < SLIDE_HISTORY_SIZE; i++) {
        if ((slide_history[i].timestamp > 0) && (now - slide_history[i].timestamp <= WINDOW_TIMEOUT)) {
            if (slide_history[i].direction == SLIDE_UP) {
                delta_speed += 1; // 上滑增加速度
            } else if (slide_history[i].direction == SLIDE_DOWN) {
                delta_speed -= 1; // 下滑减少速度
            }
        }
    }

    return delta_speed;
}

// 更新速度
void Update_Speed(void) {
    uint32_t now = HAL_GetTick();

    // 如果有滑动操作，根据方向和频率调整速度
    if (now - last_slide_time <= SLIDE_TIMEOUT) {
    	if(now - last_update_time >= WINDOW_TIMEOUT){
			int16_t delta_speed = Analyze_Slide_Events();
			// 根据滑动影响调整速度
			speed += delta_speed ;
			if (speed > MAX_SPEED) speed = MAX_SPEED;
			if (speed < MIN_SPEED) speed = MIN_SPEED;
		    last_update_time = now;
    	}
    } else {
        // 停止滑动后，模拟惯性减速
//        uint32_t elapsed_time = (now - last_update_time) / 1000.0;
        if (speed > 0) {
//            speed -= INERTIA_DECAY * elapsed_time; // 前进方向减速
        	speed -= 0.5;
            if (speed < 0) speed = 0;
        } else if (speed < 0) {
//            speed += INERTIA_DECAY * elapsed_time; // 后退方向减速
        	speed += 0.5;
            if (speed > 0) speed = 0;
        }
    }
    // 当速度为 0 时显示提示信息
    if (speed == 0) {
        LCD_ShowString(0, 0, 200, 16, 16, "Stopped ");
    }else if(speed > 0){
        LCD_ShowString(0, 0, 200, 16, 16, "Forward ");
    }else{
        LCD_ShowString(0, 0, 200, 16, 16, "Backward");
    }
}


